rs = Pin(0, Pin.OUT)
reset = Pin(1, Pin.OUT)

# Color Defination
RED = 0xf800
GREEN = 0x07e0
BLUE = 0x001f
WHITE = 0xFFFF
BLACK = 0x0000
YELLOW = 0xFFE0
GRAY0 = 0xEF7D
GRAY1 = 0x8410
GRAY2 = 0x4208

spi = SPI(0, 100000000, sck=Pin(2), mosi=Pin(3), miso=Pin(4))

def SPI_WriteData(Data):
    spi.write(Data.to_bytes(1,"big"))

def Lcd_WriteIndex(Data):
    rs.off()
    SPI_WriteData(Data)
    
def Lcd_WriteData(Data):
    rs.on()
    SPI_WriteData(Data)
    
def Lcd_WriteData_16(Data):
    rs.on()
    SPI_WriteData(Data>>8)
    SPI_WriteData(Data)

def Reset():
    reset.off()
    time.sleep_ms(100)
    reset.on()
    time.sleep_ms(100)
    
