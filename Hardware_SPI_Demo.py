def Lcd_SetRegion(x_start, y_start, x_end, y_end):
    Lcd_WriteIndex(0x2a);
    Lcd_WriteData(0x00);
    Lcd_WriteData(x_start+2);
    Lcd_WriteData(0x00);
    Lcd_WriteData(x_end+2);
    Lcd_WriteIndex(0x2b);
    Lcd_WriteData(0x00);
    Lcd_WriteData(y_start+3);
    Lcd_WriteData(0x00);
    Lcd_WriteData(y_end+3);
    Lcd_WriteIndex(0x2c);

def LCD_Clear(Color):
    Lcd_SetRegion(0,0,128-1,128-1)
    for i in range(0,128):
        for j in range(0,128):
            Lcd_WriteData_16(Color)

def PutPixel(x_start, y_start, color):
    Lcd_SetRegion(x_start,y_start,x_start+1,y_start+1)
    Lcd_WriteData_16(color)

