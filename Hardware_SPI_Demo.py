def Display_ASCII8X16(x0, y0, s, Color):
    temp = ['\0','\0']
    ywbuf = [0]*32
    
    for i in range(0, len(s)):
        if ord(s[i]) >= 161:
            temp[0] = s[i]
            temp[1] = "\0"
            return
        else:
            qm = s[i]
            ulOffset = ord(qm) * 16
            for j in range(0,16):
                ywbuf[j] = Zk_ASCII8X16[ulOffset+j]
            
            for y in range(0,16):
                for x in range(0,8):
                    k = x % 8
                    
                    if ywbuf[y]&(0x80 >> k):
                        xx=x0+x+i*8
                        PutPixel(xx,y+y0,Color)
                        
